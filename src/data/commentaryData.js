// Utility — pick random non-repeating phrase
const _lastPicked = {};
export function getCommentary(category, subcategory = null) {
  let pool;
  const map = COMMENTARY_POOLS[category];
  if (!map) return null;

  if (Array.isArray(map)) {
    pool = map;
  } else if (subcategory && map[subcategory]) {
    pool = map[subcategory];
  } else if (map._default) {
    pool = map._default;
  } else {
    return null;
  }

  if (!pool || pool.length === 0) return null;

  // Avoid repeating last phrase in same category
  const key = `${category}_${subcategory || ''}`;
  let pick;
  let attempts = 0;
  do {
    pick = pool[Math.floor(Math.random() * pool.length)];
    attempts++;
  } while (pick === _lastPicked[key] && pool.length > 1 && attempts < 5);

  _lastPicked[key] = pick;
  return pick;
}
